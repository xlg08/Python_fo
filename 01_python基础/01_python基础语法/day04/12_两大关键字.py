"""
    循环中的两大关键字：
        break ：代表终止整个循环结构
        continue ：代表终止当前本次循环，继续下一次循环
"""

'''
    break: 主要终止这个循环
    while  循环条件:
        if 满足该条件:
            break       # 跳出整个循环
            
    如果循环嵌套，break只能跳出一层循环，进入上一层循环
    嵌套循环中，外层循环一次，内层循环走一轮，，
        类似于钟表  时针 分针，秒针
'''

'''
    continue：终止本次循环
    继续下一次循环（如果满足判断条件，循环不终止）
    再continue的执行语句中也要更新计数器
'''


